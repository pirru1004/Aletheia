/* ============================================================================
 * af-footer.js — Aletheia public site footer (namespace: af-)
 * ----------------------------------------------------------------------------
 * Self-contained, no external deps. Renders a <footer> into every element
 * carrying the [data-af-footer] attribute. Injected synchronously at parse
 * time (script placed AFTER the mount point), so the "#demo" link below exists
 * before demo_scheduler.js binds its DOMContentLoaded handler to a[href="#demo"].
 *
 * ░░ ALL link targets live in ONE config block — swap them here, nowhere else ░░
 * ==========================================================================*/
const FOOTER_LINKS = {
  // CONTACT — mailto placeholders for now; will be replaced by Google Form URLs
  // this afternoon. Keep each as a single value so the swap is one line.
  contact:  'mailto:[CONTACT EMAIL]?subject=Aletheia%20enquiry',
  feedback: 'mailto:[FEEDBACK EMAIL]?subject=Aletheia%20site%20feedback',
  bookDemo: '#demo',   // real scheduler trigger: demo_scheduler.js binds a[href="#demo"]

  // SOCIAL — real URLs provided later; empty = rendered present but non-navigating.
  social: {
    linkedin: '',   // e.g. https://www.linkedin.com/company/...
    facebook: '',   // e.g. https://www.facebook.com/...
    whatsapp: ''    // click-to-chat: https://wa.me/<international number, no +/spaces>
  },

  // LEGAL — generated by generate_legal.cjs into public/legal/.
  legal: {
    terms:      '/legal/terms.html',
    privacy:    '/legal/privacy.html',
    disclaimer: '/legal/disclaimer.html'
  }
};

// Bottom line. [ENTITY NAME] stays a bracket placeholder — not invented here.
const FOOTER_ENTITY = '[ENTITY NAME]';
const FOOTER_YEAR = '[YEAR]';

(function () {
  const L = FOOTER_LINKS;

  // A social entry: live <a> when a URL is configured, otherwise a visibly
  // present but non-navigating, disabled-looking <span> (nothing looks broken).
  function socialItem(label, url) {
    if (url) {
      return `<a class="af-social" href="${url}" target="_blank" rel="noopener noreferrer">${label}</a>`;
    }
    return `<span class="af-social af-social--disabled" role="link" aria-disabled="true" title="Coming soon">${label}</span>`;
  }

  function template() {
    return `
  <div class="af-inner">
    <div class="af-cols">

      <section class="af-col" aria-labelledby="af-h-contact">
        <h2 class="af-h" id="af-h-contact">Contact</h2>
        <ul class="af-list">
          <li><a class="af-link" href="${L.contact}">Contact us</a></li>
          <li><a class="af-link" href="${L.bookDemo}">Book a demo</a></li>
          <li><a class="af-link" href="${L.feedback}">Feedback</a></li>
        </ul>
      </section>

      <section class="af-col" aria-labelledby="af-h-social">
        <h2 class="af-h" id="af-h-social">Social</h2>
        <ul class="af-list af-list--social">
          <li>${socialItem('LinkedIn', L.social.linkedin)}</li>
          <li>${socialItem('Facebook', L.social.facebook)}</li>
          <li>${socialItem('WhatsApp', L.social.whatsapp)}</li>
        </ul>
      </section>

      <section class="af-col" aria-labelledby="af-h-legal">
        <h2 class="af-h" id="af-h-legal">Terms &amp; Policies</h2>
        <ul class="af-list">
          <li><a class="af-link" href="${L.legal.terms}" target="_blank" rel="noopener">Terms of Use</a></li>
          <li><a class="af-link" href="${L.legal.privacy}" target="_blank" rel="noopener">Privacy Policy</a></li>
          <li><a class="af-link" href="${L.legal.disclaimer}" target="_blank" rel="noopener">Disclaimer</a></li>
        </ul>
      </section>

    </div>

    <div class="af-bottom">
      <span class="af-mark">ALETHEIA</span>
      <span class="af-copy">&copy; ${FOOTER_YEAR} Aletheia — ${FOOTER_ENTITY}</span>
    </div>
  </div>`;
  }

  function render() {
    const mounts = document.querySelectorAll('[data-af-footer]');
    mounts.forEach((mount) => {
      if (mount.dataset.afFooterDone) return;       // idempotent
      const footer = document.createElement('footer');
      footer.className = 'af-footer';
      footer.setAttribute('role', 'contentinfo');
      footer.setAttribute('aria-label', 'Site footer');
      footer.innerHTML = template();
      mount.appendChild(footer);
      mount.dataset.afFooterDone = '1';
    });
  }

  // Run now (mounts already parsed above this script). Fall back to
  // DOMContentLoaded only if this somehow loads before the mount exists.
  if (document.querySelector('[data-af-footer]')) {
    render();
  } else {
    document.addEventListener('DOMContentLoaded', render);
  }
})();
